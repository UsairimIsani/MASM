.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB 0Dh, 0AH, 'ENTER THE FIRST CHARACTER: $';
MSG2 DB 0DH, 0AH, 'ENTER THE SECOND CHARACTER: $';
.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX
    again:
        LEA DX, MSG1
        MOV AH, 9
        INT 21H ; PROMPTING USER TO ENTER THE FIRST CHARACTER
        MOV AH, 1
        INT 21H ; TAKING INPUT FROM THE USER
        LEA DX, MSG2
        MOV AH,9
        INT 21H ; PROMPTING USER TO ENTER THE SECOND CHARACTER
        MOV AH, 1
        INT 21H ; TAKING INPUT FROM THE USER
    JMP again ; Jump to the first instruction
MOV AH, 4CH
INT 21H
MAIN ENDP
END MAIN