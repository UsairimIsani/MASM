.MODEL SMALL
.STACK 100H
.DATA
    MSG1 DB 0Dh, 0AH, 'ENTER THE FIRST CHARACTER: $';
    MSG2 DB 0DH, 0AH, 'THE FIRST CHARACTER IS: ';
    CH1 DB ?, '$'
    MSG3 DB 0DH, 0AH, 'ENTER THE SECOND CHARACTER: $'
    MSG4 DB 0DH, 0AH, 'THE SECOND CHARACTER IS: '
    CH2 DB ?, '$'
.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX
    again:
        LEA DX, MSG1
        MOV AH, 9
        INT 21H; PROMPTING USER TO ENTER THE FIRST CHARACTER
        MOV AH, 1
        INT 21H ; TAKING INPUT FROM THE USER
        MOV CH1, AL ; UPDATING RESULT IN THE VARIABLE
        MOV BX, 0000H
        MOV BL, AL
        LEA DX, MSG2
        MOV AH, 9
        INT 21H ; OUTPUTTING THE FIRST CHARACTER ON SCREEN WITH MESSAGE
        LEA DX, MSG3
        MOV AH, 9
        INT 21H ; PROMPTING USER TO ENTER THE SECOND CHARACTER
        MOV AH, 1
        INT 21H ; TAKING INPUT FROM THE USER
        MOV CH2, AL ; UPDATING RESULT IN THE VARIABLE
        LEA DX, MSG4
        MOV AH, 9
        INT 21H ; OUTPUTTING THE SECOND CHARACTER ON SCREEN WITH MESSAGE
        MOV AH, 0
        MOV AL, CH2
        CMP AX, BX
        JNZ again
    MOV AH, 4CH
    INT 21H
MAIN ENDP
END MAIN